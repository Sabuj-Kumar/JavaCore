package com.demo.print;

public class TypeCastingDemo {

	public static void main(String[] args) {
	//Implicit
		int a = 66;
		double d = a;
		System.out.println(a);
		System.out.println(d);
		
	//Explicit
		
		double d1 = 65.67;
		int a1 = (int)d1;
		
		System.out.println(d1);
		System.out.println(a1);
		
	}
	
}
